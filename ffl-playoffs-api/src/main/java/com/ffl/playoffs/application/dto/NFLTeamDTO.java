package com.ffl.playoffs.application.dto;

/**
 * Data Transfer Object for NFL Team
 * Used for API communication
 */
public class NFLTeamDTO {
    private Long id;
    private String name;
    private String abbreviation;
    private String city;
    private String conference;
    private String division;
    private String logoUrl;
    private Integer wins;
    private Integer losses;
    private Integer ties;
    private Double winPercentage;
    private String upcomingOpponent;

    public NFLTeamDTO() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAbbreviation() {
        return abbreviation;
    }

    public void setAbbreviation(String abbreviation) {
        this.abbreviation = abbreviation;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getConference() {
        return conference;
    }

    public void setConference(String conference) {
        this.conference = conference;
    }

    public String getDivision() {
        return division;
    }

    public void setDivision(String division) {
        this.division = division;
    }

    public String getLogoUrl() {
        return logoUrl;
    }

    public void setLogoUrl(String logoUrl) {
        this.logoUrl = logoUrl;
    }

    public Integer getWins() {
        return wins;
    }

    public void setWins(Integer wins) {
        this.wins = wins;
    }

    public Integer getLosses() {
        return losses;
    }

    public void setLosses(Integer losses) {
        this.losses = losses;
    }

    public Integer getTies() {
        return ties;
    }

    public void setTies(Integer ties) {
        this.ties = ties;
    }

    public Double getWinPercentage() {
        return winPercentage;
    }

    public void setWinPercentage(Double winPercentage) {
        this.winPercentage = winPercentage;
    }

    public String getUpcomingOpponent() {
        return upcomingOpponent;
    }

    public void setUpcomingOpponent(String upcomingOpponent) {
        this.upcomingOpponent = upcomingOpponent;
    }
}
